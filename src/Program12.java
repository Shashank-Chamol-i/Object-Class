class Mutator
{
    private int id;
    private String name;
    int getId()
    {
        return id;
    }
    void setId(int id)
    {
        this.id = id;
    }
    String getName()
    {
        return name;
    }
    void setName(String name)
    {
        this.name = name;
    }

}
public class Program12 {
    public static void main(String[] args)
    {
        Mutator m = new Mutator();
        m.setId(10);
        m.setName("Shashank");
        System.out.println(m.getId());
        System.out.println(m.getName());
    }
}
