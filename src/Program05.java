class calculation
{
    void fact(int n)
    {
        int fact = 1;
        for(int i = 1;i<=n;i++)
        {
            fact = fact * i;
        }
        System.out.println("Factorial is : "+ fact);
    }
}
public class Program05 {
    public static void main(String[] args)
    {
        new calculation().fact(5);
    }
}
