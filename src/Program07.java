class Parametrized
{
    int id;
    String name;
    String address;
    Parametrized(int id,String name,String address)
    {
        this.id = id;
        this.name = name;
        this.address = address;
    }
    void display()
    {
        System.out.println("\n Id : "+this.id+"\n Name : "+this.name+"\n Address : "+this.address);
    }
}
public class Program07 {
    public static void main(String[] args)
    {
        Parametrized pp = new Parametrized(1,"Ankus","San Fransisco");
        Parametrized pp2 = new Parametrized(2,"Bilal","Brixton");
        pp.display();
        pp2.display();
    }
}
